import dotenv from "dotenv";
dotenv.config();

export async function OpenAISendBatch(batch, codebook) {
  const prompt = buildRecodingPrompt(batch, codebook);

  const model = process.env.OPENAI_MODEL || "gpt-3.5-turbo";
  const temperature = parseFloat(process.env.OPENAI_TEMPERATURE || "0.3");

  const response = await fetch("https://api.openai.com/v1/chat/completions", {
    method: "POST",
    headers: {
      Authorization: `Bearer ${process.env.OPENAI_API_KEY}`,
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      model,
      messages: [{ role: "user", content: prompt }],
      temperature,
    }),
  });

  const result = await response.json();

  if (!response.ok) {
    console.error("❌ OpenAI API error:", result);
    throw new Error(result.error?.message || "API request failed");
  }

  try {
    return JSON.parse(result.choices[0].message.content);
  } catch (err) {
    // console.error("⚠️ Invalid JSON from OpenAI:");
    // console.error(result.choices[0].message.content);
    return null;
  }
}

function buildRecodingPrompt(batch, codebook) {
  let prompt = `
You are a qualitative researcher recoding student comments using a standardized codebook created from open-coded data.

Each code has:
- a short **category name**
- a clear **definition**
- 2–3 example phrases

Use ONLY the categories in the codebook. For each student comment below, return a JSON object showing which categories apply. Assign 1–3 categories per comment. If none apply, return an empty list.

STANDARDIZED CODEBOOK:
"""
${JSON.stringify(codebook, null, 2)}
"""

Return your answer in **strict JSON**, exactly like this:

{
  "Comment 1": ["instructional clarity", "student support"],
  "Comment 2": ["engagement"]
}

⚠️ Do not include any extra explanation — return only valid JSON.

Now code the following student comments:
`;

  batch.forEach((row, i) => {
    prompt += `\nComment ${i + 1}:\n${row.text}\n`;
  });

  return prompt;
}
