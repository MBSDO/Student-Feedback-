export function RandomString(length) {
    const allowedChars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789';
    let str = '';
    for (let i = 0; i < length; i++) {
        const randomIndex = Math.floor(Math.random() * allowedChars.length);
        str += allowedChars[randomIndex];
    }
    return str;
}

export function Wait(ms) {
    return new Promise((resolve, reject) => {
        setTimeout(resolve, ms);
    });
}

export function GetUniqueValues(array) {
    const uniqueSet = new Set(array);
    const uniqueArray = [...uniqueSet];
    return uniqueArray;
}

export function ExtractIntsFromArrayOfKeys(array) {
    return array.map(str => {
        const match = str.match(/\d+/); // Match one or more digits
        return match ? parseInt(match[0]) : null; // Convert matched string to integer
    });
}

export function FindMatchingKeys(obj, pattern) {
    return Object.keys(obj).filter(key => pattern.test(key));
}